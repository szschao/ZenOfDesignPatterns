public class SuperMan{
    //超人的躯体
    private String body;
    //超人的特殊能力
    private String specialTalent;
    //超人的标志
    private String specialSymbol;

    public String getBody(){
        return this.body;
    }
    public void setBody(String _body){
        this.body=_body;
    }
    public String getSpecialTalent(){
        return this.specialTalent;
    }
    public void setSpecialTalent(String _specialTalent){
        this.specialTalent=_specialTalent;
    }
    public String getSpecialSymbol(){
        return this.specialSymbol;
    }
    public void setSpecialSymbol(String _specialSymbol){
        this.specialSymbol=_specialSymbol;
    }
}
